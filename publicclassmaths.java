class maths
	{
	public static void main(String[]args)
	{
		int n1= 5, n2=4, sum, multiply, divide, sub;

		sum = n1 + n2;
		multiply = n1*n2;
		divide = n1/n2;
		sub = n1 - n2;

		System.out.println("sum = " + sum);
		System.out.println("multiply = " + multiply);
		System.out.println("divide = " + divide);
		System.out.println("sub = " + sub);
	} 
	}